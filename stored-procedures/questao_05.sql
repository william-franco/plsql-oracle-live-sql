-- Questao 05: Log de erros em tabela de auditoria

BEGIN
  EXECUTE IMMEDIATE 'DROP PROCEDURE sp_atualizar_pedido_q05';
EXCEPTION
  WHEN OTHERS THEN NULL;
END;
/

BEGIN
  EXECUTE IMMEDIATE 'DROP TABLE LOG_ERROS_Q05';
EXCEPTION
  WHEN OTHERS THEN NULL;
END;
/

BEGIN
  EXECUTE IMMEDIATE 'DROP TABLE PEDIDOS CASCADE CONSTRAINTS';
EXCEPTION
  WHEN OTHERS THEN NULL;
END;
/

CREATE TABLE PEDIDOS AS SELECT * FROM OE.ORDERS;

CREATE TABLE LOG_ERROS_Q05 (
  log_id        NUMBER GENERATED BY DEFAULT AS IDENTITY,
  codigo_erro   NUMBER,
  mensagem_erro VARCHAR2(4000),
  data_erro     TIMESTAMP DEFAULT SYSTIMESTAMP
);

CREATE OR REPLACE PROCEDURE sp_atualizar_pedido_q05 (
  p_order_id     IN PEDIDOS.ORDER_ID%TYPE,
  p_order_status IN PEDIDOS.ORDER_STATUS%TYPE
) IS
BEGIN
  UPDATE PEDIDOS
     SET ORDER_STATUS = p_order_status
   WHERE ORDER_ID = p_order_id;

  IF SQL%ROWCOUNT = 0 THEN
    RAISE NO_DATA_FOUND;
  END IF;
EXCEPTION
  WHEN OTHERS THEN
    INSERT INTO LOG_ERROS_Q05 (codigo_erro, mensagem_erro)
    VALUES (SQLCODE, SQLERRM);
    COMMIT;
    RAISE;
END;
/

SET SERVEROUTPUT ON;

BEGIN
  sp_atualizar_pedido_q05(99999, 4);
EXCEPTION
  WHEN OTHERS THEN
    DBMS_OUTPUT.PUT_LINE('Excecao tratada apos registro em log.');
END;
/

SELECT codigo_erro, mensagem_erro FROM LOG_ERROS_Q05 ORDER BY log_id DESC FETCH FIRST 1 ROW ONLY;

ROLLBACK;
