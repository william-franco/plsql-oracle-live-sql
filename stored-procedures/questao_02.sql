-- Questao 02: AUTONOMOUS_TRANSACTION

BEGIN
  EXECUTE IMMEDIATE 'DROP TRIGGER trg_audit_salary_q02';
EXCEPTION
  WHEN OTHERS THEN NULL;
END;
/

BEGIN
  EXECUTE IMMEDIATE 'DROP PROCEDURE sp_log_salary_q02';
EXCEPTION
  WHEN OTHERS THEN NULL;
END;
/

BEGIN
  EXECUTE IMMEDIATE 'DROP TABLE LOG_SALARY_Q02';
EXCEPTION
  WHEN OTHERS THEN NULL;
END;
/

BEGIN
  EXECUTE IMMEDIATE 'DROP TABLE FUNCIONARIOS CASCADE CONSTRAINTS';
EXCEPTION
  WHEN OTHERS THEN NULL;
END;
/

CREATE TABLE FUNCIONARIOS AS SELECT * FROM HR.EMPLOYEES;

CREATE TABLE LOG_SALARY_Q02 (
  log_id      NUMBER GENERATED BY DEFAULT AS IDENTITY,
  employee_id NUMBER,
  salary_old  NUMBER,
  salary_new  NUMBER,
  data_log    TIMESTAMP DEFAULT SYSTIMESTAMP
);

CREATE OR REPLACE PROCEDURE sp_log_salary_q02 (
  p_employee_id IN NUMBER,
  p_salary_old  IN NUMBER,
  p_salary_new  IN NUMBER
) IS
  PRAGMA AUTONOMOUS_TRANSACTION;
BEGIN
  INSERT INTO LOG_SALARY_Q02 (employee_id, salary_old, salary_new)
  VALUES (p_employee_id, p_salary_old, p_salary_new);

  COMMIT;
END;
/

CREATE OR REPLACE TRIGGER trg_audit_salary_q02
AFTER UPDATE OF SALARY ON FUNCIONARIOS
FOR EACH ROW
BEGIN
  sp_log_salary_q02(:OLD.EMPLOYEE_ID, :OLD.SALARY, :NEW.SALARY);
END;
/

SET SERVEROUTPUT ON;

BEGIN
  UPDATE FUNCIONARIOS SET SALARY = SALARY + 200 WHERE EMPLOYEE_ID = 100;
  RAISE_APPLICATION_ERROR(-20002, 'Simulacao de falha na transacao principal');
EXCEPTION
  WHEN OTHERS THEN
    ROLLBACK;
    DBMS_OUTPUT.PUT_LINE('Transacao principal revertida, mas log autonomo permanece.');
END;
/

SELECT employee_id, salary_old, salary_new FROM LOG_SALARY_Q02 WHERE employee_id = 100;

ROLLBACK;
