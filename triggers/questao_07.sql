-- Questao 07: Trigger de nivel de comando (statement-level)

BEGIN
  EXECUTE IMMEDIATE 'DROP TRIGGER trg_log_update_q07';
EXCEPTION
  WHEN OTHERS THEN NULL;
END;
/

BEGIN
  EXECUTE IMMEDIATE 'DROP TABLE LOG_UPDATE_Q07';
EXCEPTION
  WHEN OTHERS THEN NULL;
END;
/

BEGIN
  EXECUTE IMMEDIATE 'DROP TABLE PEDIDOS CASCADE CONSTRAINTS';
EXCEPTION
  WHEN OTHERS THEN NULL;
END;
/

CREATE TABLE PEDIDOS AS SELECT * FROM OE.ORDERS;

CREATE TABLE LOG_UPDATE_Q07 (
  log_id        NUMBER GENERATED BY DEFAULT AS IDENTITY,
  comando       VARCHAR2(20),
  data_execucao TIMESTAMP DEFAULT SYSTIMESTAMP
);

CREATE OR REPLACE TRIGGER trg_log_update_q07
AFTER UPDATE ON PEDIDOS
BEGIN
  INSERT INTO LOG_UPDATE_Q07 (comando) VALUES ('UPDATE');
END;
/

UPDATE PEDIDOS SET ORDER_TOTAL = ORDER_TOTAL WHERE SALES_REP_ID = 153;

SELECT COUNT(*) AS registros_log FROM LOG_UPDATE_Q07;

ROLLBACK;
