-- Questao 02: Trigger AFTER INSERT para auditoria

BEGIN
  EXECUTE IMMEDIATE 'DROP TRIGGER trg_audit_pedidos_q02';
EXCEPTION
  WHEN OTHERS THEN NULL;
END;
/

BEGIN
  EXECUTE IMMEDIATE 'DROP TABLE AUDIT_PEDIDOS_Q02';
EXCEPTION
  WHEN OTHERS THEN NULL;
END;
/

BEGIN
  EXECUTE IMMEDIATE 'DROP TABLE PEDIDOS CASCADE CONSTRAINTS';
EXCEPTION
  WHEN OTHERS THEN NULL;
END;
/

CREATE TABLE PEDIDOS AS SELECT * FROM OE.ORDERS;

CREATE TABLE AUDIT_PEDIDOS_Q02 (
  audit_id      NUMBER GENERATED BY DEFAULT AS IDENTITY,
  order_id      NUMBER,
  order_date    DATE,
  order_status  NUMBER,
  order_total   NUMBER,
  data_auditoria TIMESTAMP DEFAULT SYSTIMESTAMP
);

CREATE OR REPLACE TRIGGER trg_audit_pedidos_q02
AFTER INSERT ON PEDIDOS
FOR EACH ROW
BEGIN
  INSERT INTO AUDIT_PEDIDOS_Q02 (
    order_id, order_date, order_status, order_total
  ) VALUES (
    :NEW.ORDER_ID, :NEW.ORDER_DATE, :NEW.ORDER_STATUS, :NEW.ORDER_TOTAL
  );
END;
/

