-- Questao 04: Trigger AFTER DELETE para log

BEGIN
  EXECUTE IMMEDIATE 'DROP TRIGGER trg_log_delete_q04';
EXCEPTION
  WHEN OTHERS THEN NULL;
END;
/

BEGIN
  EXECUTE IMMEDIATE 'DROP TABLE LOG_PEDIDOS_Q04';
EXCEPTION
  WHEN OTHERS THEN NULL;
END;
/

BEGIN
  EXECUTE IMMEDIATE 'DROP TABLE PEDIDOS CASCADE CONSTRAINTS';
EXCEPTION
  WHEN OTHERS THEN NULL;
END;
/

CREATE TABLE PEDIDOS AS SELECT * FROM OE.ORDERS;

CREATE TABLE LOG_PEDIDOS_Q04 (
  log_id        NUMBER GENERATED BY DEFAULT AS IDENTITY,
  order_id      NUMBER,
  order_total   NUMBER,
  usuario       VARCHAR2(128),
  data_exclusao TIMESTAMP
);

CREATE OR REPLACE TRIGGER trg_log_delete_q04
AFTER DELETE ON PEDIDOS
FOR EACH ROW
BEGIN
  INSERT INTO LOG_PEDIDOS_Q04 (
    order_id, order_total, usuario, data_exclusao
  ) VALUES (
    :OLD.ORDER_ID, :OLD.ORDER_TOTAL, USER, SYSTIMESTAMP
  );
END;
/

INSERT INTO PEDIDOS (
  ORDER_ID, ORDER_DATE, ORDER_MODE, CUSTOMER_ID, ORDER_STATUS, ORDER_TOTAL
) VALUES (
  99989, SYSDATE, 'direct', 101, 0, 1800
);

DELETE FROM PEDIDOS WHERE ORDER_ID = 99989;

SELECT * FROM LOG_PEDIDOS_Q04 WHERE order_id = 99989;

ROLLBACK;
