-- Questao 09: Trigger composto (compound trigger)

BEGIN
  EXECUTE IMMEDIATE 'DROP TRIGGER trg_compound_pedidos_q09';
EXCEPTION
  WHEN OTHERS THEN NULL;
END;
/

BEGIN
  EXECUTE IMMEDIATE 'DROP TABLE LOG_COMPOUND_Q09';
EXCEPTION
  WHEN OTHERS THEN NULL;
END;
/

BEGIN
  EXECUTE IMMEDIATE 'DROP TABLE PEDIDOS CASCADE CONSTRAINTS';
EXCEPTION
  WHEN OTHERS THEN NULL;
END;
/

CREATE TABLE PEDIDOS AS SELECT * FROM OE.ORDERS;

CREATE TABLE LOG_COMPOUND_Q09 (
  log_id        NUMBER GENERATED BY DEFAULT AS IDENTITY,
  mensagem      VARCHAR2(200),
  total_lote    NUMBER,
  data_registro TIMESTAMP DEFAULT SYSTIMESTAMP
);

CREATE OR REPLACE TRIGGER trg_compound_pedidos_q09
FOR INSERT OR UPDATE OF ORDER_TOTAL ON PEDIDOS
COMPOUND TRIGGER
  g_total_lote NUMBER := 0;

  BEFORE STATEMENT IS
  BEGIN
    g_total_lote := 0;
  END BEFORE STATEMENT;

  BEFORE EACH ROW IS
  BEGIN
    IF :NEW.ORDER_TOTAL < 0 THEN
      RAISE_APPLICATION_ERROR(-20009, 'ORDER_TOTAL nao pode ser negativo.');
    END IF;
  END BEFORE EACH ROW;

  AFTER EACH ROW IS
  BEGIN
    g_total_lote := g_total_lote + NVL(:NEW.ORDER_TOTAL, 0);
  END AFTER EACH ROW;

  AFTER STATEMENT IS
  BEGIN
    INSERT INTO LOG_COMPOUND_Q09 (mensagem, total_lote)
    VALUES ('Operacao em lote concluida', g_total_lote);
  END AFTER STATEMENT;
END trg_compound_pedidos_q09;
/

UPDATE PEDIDOS
   SET ORDER_TOTAL = ORDER_TOTAL
 WHERE ORDER_ID IN (2459, 2460);

SELECT mensagem, total_lote FROM LOG_COMPOUND_Q09 ORDER BY log_id DESC FETCH FIRST 1 ROW ONLY;

ROLLBACK;
