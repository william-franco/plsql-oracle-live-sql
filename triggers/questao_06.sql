-- Questao 06: Trigger de linha com :OLD e :NEW

BEGIN
  EXECUTE IMMEDIATE 'DROP TRIGGER trg_hist_salary_q06';
EXCEPTION
  WHEN OTHERS THEN NULL;
END;
/

BEGIN
  EXECUTE IMMEDIATE 'DROP TABLE HIST_SALARY_Q06';
EXCEPTION
  WHEN OTHERS THEN NULL;
END;
/

BEGIN
  EXECUTE IMMEDIATE 'DROP TABLE FUNCIONARIOS CASCADE CONSTRAINTS';
EXCEPTION
  WHEN OTHERS THEN NULL;
END;
/

CREATE TABLE FUNCIONARIOS AS SELECT * FROM HR.EMPLOYEES;

CREATE TABLE HIST_SALARY_Q06 (
  hist_id     NUMBER GENERATED BY DEFAULT AS IDENTITY,
  employee_id NUMBER,
  salary_old  NUMBER,
  salary_new  NUMBER,
  data_alteracao TIMESTAMP DEFAULT SYSTIMESTAMP
);

CREATE OR REPLACE TRIGGER trg_hist_salary_q06
AFTER UPDATE OF SALARY ON FUNCIONARIOS
FOR EACH ROW
WHEN (NEW.SALARY <> OLD.SALARY)
BEGIN
  INSERT INTO HIST_SALARY_Q06 (employee_id, salary_old, salary_new)
  VALUES (:OLD.EMPLOYEE_ID, :OLD.SALARY, :NEW.SALARY);
END;
/

UPDATE FUNCIONARIOS SET SALARY = SALARY + 500 WHERE EMPLOYEE_ID = 100;

SELECT * FROM HIST_SALARY_Q06 WHERE employee_id = 100;

ROLLBACK;
